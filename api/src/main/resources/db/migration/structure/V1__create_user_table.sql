CREATE TABLE `t_user` (
    `ID` int(11) NOT NULL AUTO_INCREMENT,
    `USERNAME` varchar(255) NOT NULL,
    `USER_TYPE` varchar(100) NOT NULL,
    `FIRST_NAME` varchar(255) NOT NULL,
    `LAST_NAME` varchar(255) NOT NULL,
    `EMAIL` varchar(255) DEFAULT NULL,
    `PASSWORD` varchar(255) NOT NULL,
    `STATUS` char(3) DEFAULT 'ACT',
    `LINK_EXPIRE_TIME` datetime DEFAULT NULL,
    `CREATED_BY` int(11) NOT NULL,
    `CREATED_DATE` datetime NOT NULL,
    `MODIFIED_BY` int(11) DEFAULT NULL,
    `MODIFIED_DATE` datetime DEFAULT NULL,
    `VERSION` bigint(20) NOT NULL,
    PRIMARY KEY (`ID`),
    UNIQUE KEY `T_USER_UQ` (`USERNAME`),
    KEY `T_USER_FK1` (`CREATED_BY`),
    KEY `T_USER_FK2` (`MODIFIED_BY`),
    CONSTRAINT `T_USER_FK1` FOREIGN KEY (`CREATED_BY`) REFERENCES `t_user` (`ID`),
    CONSTRAINT `T_USER_FK2` FOREIGN KEY (`MODIFIED_BY`) REFERENCES `t_user` (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;